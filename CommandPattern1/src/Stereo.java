
public class Stereo {

		public void on() {
			System.out.println("Turning stereo ON");
		}
		
		public void setCD() {
			System.out.println("Setting a CD in the stereo");
		}
		
		public void off() {
		System.out.println("Turning stereo OFF");
		}
		
		public void ejectCD() {
			
			System.out.println("Ejecting the CD");
		}
		
		public void setVolume() {
			System.out.println("Setting the volume on 10");
		}
		
		public void setVolumeZero() {
			System.out.println("Setting the volume to ZERO");
		}
}
