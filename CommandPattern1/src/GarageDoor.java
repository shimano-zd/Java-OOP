
public class GarageDoor {

		public void open() {
			System.out.println("Doors are openening...");
		}
		
		public void close() {
			System.out.println("Doors are closing...");
		}
		
		public void lightOn() {
			System.out.println("Garage lights are on...");
		}
		
		public void lightOff() {
			System.out.println("Shutting down garage lights...");
		}
		
		public void stop() {
			System.out.println("Stop the garage door");
		}
}
